public class OperadoresComparacion {
    public static void main(String args[]) {
        // Operadores de comparacion
        int a = 1;
        int b = 2;

        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a == b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a != b);
    }
}
