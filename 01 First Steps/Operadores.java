public class Operadores {
    public static void main(String args[]) {
        // Operadores aritmeticos
        int suma = 10 + 20;
        int resta = 20 - 10;
        int multiplicacion = 20 * 10;
        int division = 20 / 10;
        int modulo = 20 % 10;

        // Incremento y Decrementos
        // Utiliza y despues hace la operacion
        int count = 0;
        // count = count + 1
        count++;
        // count = count - 1
        count--;
        
        // PreIncremento y PreDecrementos
        // Igual solo que primero hace la operacion y luego utiliza
        ++count;
        --count;

        // count = count + 5
        count += 5;
        count -= 5;
        count *= 5;
        count /= 5;
    }
}
